def addition(num1,num2):
    sum = num1+num2
    return sum

def substration(num1,num2):
    sub = num1 - num2
    return sub

def multipication(num1,num2):
    mul = num1*num2
    return mul

def division(num1,num2):
    div = num1/num2
    return div


print(addition(10,30),
substration(30,10),
multipication(10,20),
division(100,40))